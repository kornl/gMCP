}

getAllMatrices <- function() {
	objects <- ls(globalenv())
	matrices <- c()
	for (obj in objects) {
		if (is.matrix(get(obj, envir=globalenv()))) matrices <- c(matrices, obj)
	}
	return(matrices)
}